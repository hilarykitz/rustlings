#[derive(Debug)]
struct Rectangle {
    width: u32,
    height: u32,
}

impl Rectangle {
    fn can_hold(&self, other: &Rectangle) -> bool {
        self.width > other.width && self.height > other.height
    }
}

#[cfg(test)]
mod tests {
    use Rectangle;

    #[test]
    fn you_can_assert() {
        let larger = Rectangle {
            width: 8,
            height: 7,
        };
        let smaller = Rectangle {
            width: 5,
            height: 1,
        };

        let bigger = Rectangle {
            width: 9,
            height: 8,
        };

        assert!(larger.can_hold(&smaller));
    }
}
